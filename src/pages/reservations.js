export function createReservations() {
  const container = document.querySelector(".container");
  const h1 = document.querySelector("h1");
  h1.textContent = "Reservations";

  const topCont = document.createElement("div");
  topCont.className = "top-container";
  topCont.setAttribute("data-reserve", "");

  const div = document.createElement("div");
  div.className = "textbox";

  const h2 = document.createElement("h2");
  h2.textContent = "Reserve Your Table at Mishin";
  const para = document.createElement("p");
  para.textContent =
    "Join us for an unforgettable dining experience. To guarantee your spot and enjoy our dishes at their best, please book your table in advance. We look forward to welcoming you.";

  const divContact = createContactDiv();

  div.appendChild(h2);
  div.appendChild(para);
  topCont.appendChild(div);
  topCont.appendChild(divContact);

  const infoCont = document.createElement("div");
  infoCont.className = "info-container";
  const location = createInfoBlock("Location", [
    "128 Orchard St, New York, NY 10002 (Lower East Side, Manhattan)",
  ]);
  const hours = createInfoBlock("Hours", [
    "Monday - Thursday:  7PM - 1 AM",
    "Friday - Sunday: 7PM - 3 AM",
  ]);

  infoCont.appendChild(location);
  infoCont.appendChild(hours);

  const imageCont = document.createElement("div");
  imageCont.className = "map-img";

  container.appendChild(topCont);
  container.appendChild(infoCont);
  container.appendChild(imageCont);
}

